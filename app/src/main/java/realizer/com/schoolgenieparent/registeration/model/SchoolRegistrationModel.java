public class SchoolRegistrationModel {

    public String Code ="";
    public String name ="";
    public String Address ="";
    public String ContactNo ="";
    public String EmailId ="";
    public String ContactPerson ="";
    public String CommentsForCommunication ="";
    public String Std ="";
    public String Div ="";


    public String getCode() {
        return Code;
    }

    public void setCode(String code) {
        Code = code;
    }

    public String getName() {
        return name;
    }

    public void setName(String name) {
        this.name = name;
    }

    public String getAddress() {
        return Address;
    }

    public void setAddress(String address) {
        Address = address;
    }

    public String getContactNo() {
        return ContactNo;
    }

    public void setContactNo(String contactNo) {
        ContactNo = contactNo;
    }

    public String getEmailId() {
        return EmailId;
    }

    public void setEmailId(String emailId) {
        EmailId = emailId;
    }

    public String getContactPerson() {
        return ContactPerson;
    }

    public void setContactPerson(String contactPerson) {
        ContactPerson = contactPerson;
    }

    public String getCommentsForCommunication() {
        return CommentsForCommunication;
    }

    public void setCommentsForCommunication(String commentsForCommunication) {
        CommentsForCommunication = commentsForCommunication;
    }

    public String getStd() {
        return Std;
    }

    public void setStd(String std) {
        Std = std;
    }

    public String getDiv() {
        return Div;
    }

    public void setDiv(String div) {
        Div = div;
    }
}
